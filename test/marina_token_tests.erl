-include("test.hrl").

%% tests
token_test() ->
    assert_m3p(-9223372035861886709,
        <<9, 131, 77, 226, 102, 103, 17, 231, 140, 250, 183, 95, 66, 0,
        24, 117>>),
    assert_m3p(-9223369077907327739,
        <<160, 39, 52, 130, 163, 205, 17, 230, 164, 217, 118, 211, 94, 3,
        67, 199>>),
    assert_m3p(-5980844116634242428,
        <<233, 31, 36, 72, 86, 15, 17, 231, 191, 184, 237, 33, 29, 0, 18, 21>>),
    assert_m3p(2867464136704746769,
        <<166, 28, 198, 70, 141, 130, 17, 231, 158, 157, 233, 18, 139, 1,
        113, 126>>).

%% private
assert_m3p(Expected, Key) ->
    ?assertEqual(Expected, marina_token:m3p(Key)).
